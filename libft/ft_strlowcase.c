char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if ('A' <= *str && *str <= 'Z')
			*str = *str + 32;
		str++;
		i++;
	}
	str = str - i;
	return (str);
}
