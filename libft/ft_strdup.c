#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int i;
	char*string;

	i = 0;
	while (s1[i] != '\0')
		i++;
	string = malloc((i + 1) * sizeof(char));
	if (string == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		string[i] = s1[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
