#include <unistd.h>

void	ft_putendl(char const *s)
{
	unsigned long	i;
	char			c;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		c = s[i];
		write(1, &c, 1);
		i++;
	}
	write(1, "\n", 1);
}
