#include <string.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned long	i;
	char			*d;
	const	char	*s;

	d = (char *)dst;
	s = (const char *)src;
	i = 0;
	if (dst == src)
		return (dst);
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
