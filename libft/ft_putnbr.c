#include <unistd.h>

void	ft_putnbr(int nb)
{
	char n;

	if (nb == -2147483648)
	{
		nb = 147483648;
		write(1, "-", 1);
		write(1, "2", 1);
	}
	if (nb < 0)
	{
		nb = nb * (-1);
		write(1, "-", 1);
	}
	if (nb / 10)
		ft_putnbr(nb / 10);
	n = '0' + nb % 10;
	write(1, &n, 1);
}
