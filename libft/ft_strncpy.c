#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	unsigned long i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
